// operasi tambah 2 bilangan
function tambah(angka1, angka2) {
  return angka1 + angka2
}

// operasi kurang 2 bilangan
function kurang(angka1, angka2) {
  return angka1 - angka2
}

// operasi kali 2 bilangan
function kali(angka1, angka2) {
  return angka1 * angka2
}

// operasi bagi 2 bilangan
function bagi(angka1, angka2) {
  return angka1 / angka2
}

// operasi sisa hasil bagi 2 bilangan
function mod(angka1, angka2) {
  return angka1 % angka2
}

console.log(tambah(91, 27));
console.log(kurang(91, 27));
console.log(kali(91, 27));
console.log(bagi(10, 2));
console.log(mod(93, 6));