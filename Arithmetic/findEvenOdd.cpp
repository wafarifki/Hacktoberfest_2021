#include <bits/stdc++.h>
using namespace std;

int main()
{
    int num;
    cout<<"Enter the Number to check whether it is Even or Odd : ";
    cin>>num;
    if(num % 2 == 0){
        cout<<"The Number is Even";
    }
    else{
        cout<<"The Number is Odd";
    }
    return 0;
}